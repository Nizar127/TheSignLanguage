package com.balqis.wetalklatest;

public class wordData {
    String ID;
    String name;
    String word;

    public wordData() {
        this.ID = ID;
        this.name = name;
        this.word = word;
    }

    public String getID() {
        return ID;
    }

    public void setID(String ID) {
        this.ID = ID;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getWord() {
        return word;
    }

    public void setWord(String word) {
        this.word = word;
    }
}
